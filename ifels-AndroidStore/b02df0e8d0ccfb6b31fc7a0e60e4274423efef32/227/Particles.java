package org.ourunix.android.particles;

public class Particles {
	boolean active; // 是否激活
	float life; // 粒子生命
	float fade; // 衰减速度

	float r; // 红色值
	float g; // 绿色值
	float b; // 蓝色值

	float x; // X位置
	float y; // Y位置
	float z; // Z位置

	float xi; // X方向速度
	float yi; // Y方向速度
	float zi; // Z方向速度

	float xg; // X 方向重力加速度
	float yg; // Y 方向重力加速度
	float zg; // Z 方向重力加速度
}
